;; Hello! This is a clojure calculator I made while I was learning clojure. It only has a few
;; basic functions, such as 'add', 'subtract', 'multiply', 'divide'. So as you can see a very
;; basic calculator.

;; Made by nvttles (2025)

(defn add [a b] 
    (+ a b))

(defn subtract [a b] 
    (- a b))

(defn multiply [a b] 
    (* a b))

(defn divide [a b] 
    (/ a b))
